     * Display the specified resource.
     *
     * @param  \App\Models\Car  $car
     * @return \Illuminate\Http\Response
     */
    public function show(Car $car)
    {
        // To make sure you wont be able to rent a already rented a query is used that
        // checks if the car has any on going invoices attached
        $rented = $car->where("id", "=", $car->id)->whereHas("invoice", function($q){
            $q->where('rentStart', '<', now())
            ->where( "rentEnd", ">", now());
})->exists();
        return view("car.show")->with(["car" => $car, "rented" => $rented]);
    }

